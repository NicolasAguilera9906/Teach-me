package edu.eci.arsw.teachtome.model;

/**
 * Clase que representa un punto de un dibujo dentro de la aplicaci√≥n TeachToMe
 */
public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
